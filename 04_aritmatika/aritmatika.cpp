#include <iostream>

using namespace std;

int main()
{
    int a = 6;
    int b = 5;
    int hasil;

    // penjumlahan
    hasil = a + b;
    cout << hasil << endl;

    // pengurangan
    hasil = a - b;
    cout << hasil << endl;

    // perkalian
    hasil = a * b;
    cout << hasil << endl;

    // pembagian
    hasil = a / b;
    cout << hasil << endl;

    // sisa bagi
    hasil = a % b;
    cout << hasil << endl;

    cin.get();
    return 0;
}