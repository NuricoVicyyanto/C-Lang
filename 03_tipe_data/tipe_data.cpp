#include <iostream>

using namespace std;

int main()
{
    // integer
    unsigned int a = 1; // unsigned untuk menghiangkan negatif dan dua kali lipat limit maksimal

    // long
    long b = 6;

    // short
    short c = 7;

    // float
    float d = 8.0;

    // double
    double e = 9.0;

    // char
    char f = 'a';

    // boolean
    bool g = true;

    cout << g << endl;
    cout << sizeof(g) << " byte" << endl;
    cin.get();
    return 0;
}