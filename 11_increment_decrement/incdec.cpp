# include <iostream>

using namespace std;

int main()
{
    // increment and decrement
    // preincrement and postincrement

    int a = 8;
    int b = 9;

    // post increment
    cout << a << endl;
    cout << a++ << endl;
    cout << a << endl;

    cout << "========" << endl;

    cout << b << endl;
    cout << ++b << endl;
    cout << b << endl;

}